# Read existing product names for dedup
$existingNames = @()
$lines = Get-Content $file
foreach ($line in $lines) {
    if ($line -match "nombre:\s*'([^']+)'") {
        $existingNames += $matches[1].ToLower()
    }
}

# Filter duplicates
$toAdd = @()
foreach ($p in $newProducts) {
    if ($existingNames -notcontains $p.n.ToLower()) {
        $toAdd += $p
    }
}

Write-Host "Existing: $($existingNames.Count) | New provided: $($newProducts.Count) | To add: $($toAdd.Count)"

if ($toAdd.Count -eq 0) {
    Write-Host "Nothing to add."
    exit 0
}

# Group by provider
$grouped = $toAdd | Group-Object { $_.p }

# Build lines
$newLines = @()
foreach ($group in $grouped) {
    $prov = $group.Name
    $newLines += ""
    $newLines += "  // -- $prov (nuevos) --"
    foreach ($item in $group.Group) {
        $nombre = $item.n
        $compra = $item.c
        $venta = $item.v
        $pad = ' ' * [Math]::Max(1, 35 - $nombre.Length)
        $newLines += "  { nombre: '$nombre',${pad}proveedor: '$prov', compra: $compra, venta: $venta, unidad: 'und' },"
    }
}

# Insert before ];
$content = Get-Content $file -Raw
$content = $content.TrimEnd()
$content = $content -replace '\];\s*$', ''
$content = $content.TrimEnd()
if (-not $content.EndsWith(',')) { $content += ',' }
$content += "`n" + ($newLines -join "`n") + "`n];`n"

Set-Content -Path $file -Value $content -Encoding UTF8 -NoNewline
Write-Host "Done!"
